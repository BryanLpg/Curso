# Operadores aritmeticos

# Operador +  Realiza suma Ejemplo 2 + 3 
print(17+5)
# Operador -  Realiza resta Ejemplo 2 - 3
print(17-5)
# Operador *  Realiza multiplicacion Ejemplo 2 * 3
print(17*5)
# Operador /  Realiza division Ejemplo 2 / 3
print(17/5)
# Operador %  Realiza modulo Ejemplo 2 % 3
print(17%5)
# Operador ** Realiza Exponentiation Ejemplo 2 ** 3
print(10**600)
# Operador // Realiza divicion entera Ejemplo 2 // 3
print(17//5)

